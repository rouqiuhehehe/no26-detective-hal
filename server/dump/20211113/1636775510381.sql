CREATE TABLE IF NOT EXISTS `opera_list` (
  `opera_id` bigint NOT NULL AUTO_INCREMENT COMMENT 'root_id',
  `id` varchar(100) DEFAULT NULL COMMENT 'id',
  `partner_id` varchar(100) DEFAULT NULL COMMENT 'null',
  `goods_init_id` varchar(100) DEFAULT NULL COMMENT 'null',
  `name` varchar(100) DEFAULT NULL COMMENT '本名',
  `vip_price` bigint DEFAULT NULL COMMENT 'vip价格',
  `sale_price` bigint DEFAULT NULL COMMENT '普通价格',
  `category_ids` varchar(100) DEFAULT NULL COMMENT '类型编号',
  `default_category_ids` varchar(100) DEFAULT NULL COMMENT '默认类型编号',
  `goods_label_ids` varchar(100) DEFAULT NULL,
  `goods_numbers_id` varchar(100) DEFAULT NULL COMMENT 'null',
  `man` bigint DEFAULT NULL COMMENT '男',
  `woman` bigint DEFAULT NULL COMMENT '女',
  `galley` varchar(100) DEFAULT NULL,
  `galley_people` varchar(100) DEFAULT NULL,
  `pic_url` varchar(100) DEFAULT NULL COMMENT 'pic地址',
  `game_time` varchar(100) DEFAULT NULL COMMENT '游戏时间',
  `difficulty` bigint DEFAULT NULL COMMENT '难度 1新手 2进阶 3烧脑',
  `detail` varchar(100) DEFAULT NULL COMMENT '备注',
  `browser_num` bigint DEFAULT NULL COMMENT '0',
  `sales_num` bigint DEFAULT NULL COMMENT '0',
  `issue_date` varchar(100) DEFAULT NULL COMMENT 'null',
  `status` bigint DEFAULT NULL COMMENT '0',
  `heat` bigint DEFAULT NULL COMMENT '0',
  `score` bigint DEFAULT NULL COMMENT '0',
  `sort` bigint DEFAULT NULL COMMENT '0',
  `remark` varchar(100) DEFAULT NULL,
  `flag` bigint DEFAULT NULL COMMENT '0',
  `create_time` varchar(100) DEFAULT NULL COMMENT 'null',
  `update_time` varchar(100) DEFAULT NULL COMMENT 'null',
  `recommend` varchar(100) DEFAULT NULL COMMENT '标签 1无标签 2店长推荐 3热 4新 5预告',
  `recommend_sort` bigint DEFAULT NULL COMMENT '0',
  `history_price` bigint DEFAULT NULL COMMENT '历史价格',
  `goods_qr` varchar(100) DEFAULT NULL,
  `goods_sort` bigint DEFAULT NULL COMMENT '0',
  `is_top` bigint DEFAULT NULL COMMENT '是否置顶 0否 1是',
  `forward_goods_id` varchar(100) DEFAULT NULL,
  `vip_price_status` bigint DEFAULT NULL COMMENT '0',
  `focus_number` bigint DEFAULT NULL COMMENT '想玩人数',
  `focus_status` bigint DEFAULT NULL COMMENT '0',
  `play_number` bigint DEFAULT NULL COMMENT '已玩人数',
  `play_status` bigint DEFAULT NULL COMMENT '0',
  `catalogs_names` varchar(100) DEFAULT NULL,
  `default_catalogs_names` varchar(100) DEFAULT NULL COMMENT '本类型',
  `is_city_limit` bigint DEFAULT NULL COMMENT '是否城限 0否 1是',
  `is_exclusive` bigint DEFAULT NULL COMMENT '是否独家 0否 1是',
  `is_real` varchar(100) DEFAULT NULL COMMENT 'null',
  `number` bigint DEFAULT NULL COMMENT '0',
  PRIMARY KEY (`opera_id`) USING BTREE
) ENGINE = InnoDB AUTO_INCREMENT = 90 DEFAULT CHARSET = utf8mb3 COMMENT = 'opera_list';

